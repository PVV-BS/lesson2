package ru.sber.javashcool;

public class Calculator {


    public static Integer add(Integer Value1, Integer Value2)
    {
        return Value1 + Value2;
    }

    public static int add(int Value1, int Value2)
    {
        return Value1 + Value2;
    }

    public static Integer subtract(Integer Value1, Integer Value2)
    {
        return Value1 - Value2;
    }

    public static int subtract(int Value1, int Value2)
    {
        return Value1 - Value2;
    }

    public static Integer multiply(Integer Value1, Integer Value2)
    {
        return Value1 * Value2;
    }

    public static int multiply(int Value1, int Value2)
    {
        return Value1 * Value2;
    }

    public static Integer divide(Integer Value1, Integer Value2)
    {
        return Value1 / Value2;
    }

    public static int divide(int Value1, int Value2)
    {
        return Value1 / Value2;
    }

    public static Integer sqr(Integer Value)
    {
        return Value*Value;
    }

    public static int sqr(int Value)
    {
        return Value*Value;
    }

    public static Integer sqrt(Integer Value)
    {
        return (int) Math.sqrt(Value);
    }

    public static int sqrt(int Value)
    {
        return (int) Math.sqrt(Value);
    }

    public static Double sqrt(Double Value)
    {
        return Math.sqrt(Value);
    }

    public static double sqrt(double Value)
    {
        return (int) Math.sqrt(Value);
    }

}
